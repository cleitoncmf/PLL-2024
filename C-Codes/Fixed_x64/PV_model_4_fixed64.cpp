void PV_model_4_fixed64(uint1_t sinc,
                        data_t G,
                        data_t T,
                        data_t Vpv,
                        data_t *Ipv,
                        data_t *IG,
                        data_t *ID,
						data_t *arg_exp){



    static uint1_t aux_sinc;
    
    // Inner variables    
    static data_t ipv_aux;
    static data_t vcell;
    static data_t vd;
    static data_t id_aux;
    static data_t iG_aux;

    static data_t arg_exp_aux;


    
    

    if(aux_sinc == sinc){
        //faça nada
    }
    else{

    	aux_sinc = sinc;


        // PV model
        vcell = overN*Vpv;

        vd = vcell + Rs*ipv_aux;

        arg_exp_aux = vd*overa*overVt_LUT(T) + get_lnI0(T);

        if(arg_exp_aux < data_t(-20)){
        	arg_exp_aux = data_t(-20);
        }

        if(arg_exp_aux > data_t(5)){
        	arg_exp_aux = data_t(5);
        }


        id_aux = ( exp_LUT( arg_exp_aux ) - get_I0(T) );

        iG_aux = G*IrefoverGref + CT*(T-Tref);


        if(id_aux > iG_aux){
        	id_aux = iG_aux - data_t(0.01);
        }

        ipv_aux = iG_aux - id_aux - vd*overRsh;



        



    }

    *Ipv = ipv_aux;
    *ID = id_aux;
    *IG = iG_aux;
    
    *arg_exp = arg_exp_aux;



}





